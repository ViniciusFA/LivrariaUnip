    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
       
       try{
            //1- Guardar os dados do objeto i na tela Incluir
            Incluir i = new Incluir();
            i.setIsbn(Integer.parseInt(jTextField1.getText()));
            i.setTitulo(jTextField2.getText());
            i.setAutor(jTextField3.getText());
            i.setEditora(jTextField4.getText());
            i.setPreco(Float.parseFloat(jTextField5.getText()));

            //2- Criar um objeto do tipo QueriesDao
            QueriesDao dao = new QueriesDao();
            dao.cadastrarLivro(i);
            //3- mensagem avisando que deu tudo certo
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
       }catch(Exception erro){
           //mensagem se der algum erro
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar" + erro);
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

