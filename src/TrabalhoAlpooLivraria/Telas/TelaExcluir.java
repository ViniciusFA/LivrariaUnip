    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      Connection con = null;
      FabricaConexao f = new FabricaConexao();
      con = f.Conexao();
        try{
            String sql = "select * from Livro where isbn =  '" +
                    jTextField1.getText()+"'";
          PreparedStatement prep = con.prepareStatement(sql);
          ResultSet rs = prep.executeQuery();
          
        
          while(rs.next()){
              String isbn = rs.getString("isbn");
              String titulo = rs.getString("titulo");
              String autor = rs.getString("autor");
              String editora = rs.getString("editora");
              String preco = rs.getString("preco");
              jTextField2.setText(isbn);
              jTextField2.setEnabled(false);
              
              jTextField3.setText(titulo);
              //jTextField3.setEnabled(false);
                            
              jTextField4.setText(autor);
              //jTextField4.setEnabled(false);
              
              jTextField5.setText(editora);
              //jTextField5.setEnabled(false);
              
              jTextField6.setText(preco);
              //jTextField6.setEnabled(false);
          }
         }catch(SQLException erro){
            System.out.println("Ocorreu um erro"+ erro.getMessage());
            erro.printStackTrace();
         }catch(Exception e){
             e.printStackTrace();
            }
    
    }//GEN-LAST:event_jButton1ActionPerformed

